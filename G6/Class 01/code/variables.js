//Numbers
let num = 7; //declaration & initiazlization
console.log(num);
console.log("Type of num: ");
console.log(typeof(num));

//Strings

let stringNum = '7'; //string beacause we use quotes
console.log(stringNum);
console.log("Type of stringNum:");
console.log(typeof(stringNum));

stringNum = 5; // a variable can change the value and the type
console.log(stringNum);
console.log("Type of stringNum:");
console.log(typeof(stringNum));

let firstName = "Ana"; //string with double quotes
console.log(firstName);
console.log("Type of first name:");
console.log(typeof(firstName));

//we can change the value of a variable, but we cannot decalre the same variable twice
firstName = 'Petko'; //string, with single quotes
console.log(firstName);
console.log("Type of first name:");
console.log(typeof(firstName));

